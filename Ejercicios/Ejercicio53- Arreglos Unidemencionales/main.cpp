#include <iostream>

using namespace std;

int main(int argc, char const *argv[])
{   
    // Declaracion del Arreglo
    int numeros [5];
    
    // Asignacion de valores a un arreglo unidimencional
    numeros [0] = 10;
    numeros [1] = 15;
    numeros [2] = 20;
    numeros [3] = 25;
    numeros [4] = 30;
    
    // Lectura de los valores de un arreglo unidimencional (Vector)
    cout << numeros [0] << endl;
    cout << numeros [1] << endl;
    cout << numeros [2] << endl;
    cout << numeros [3] << endl;
    cout << numeros [4] << endl;
    
    return 0;
}